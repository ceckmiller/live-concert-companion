/**
 * @param {string} artistSlug
 * @param {string} songTitle
 * @returns {string | null}
 */
export function getOfficialVideo(artistSlug, songTitle) {
  const catalog = OFFICIAL_VIDEOS[artistSlug];
  if (!catalog) return null;
  if (catalog[songTitle]) return catalog[songTitle];
  const stripped = songTitle.replace(/\s*\(cover\)$/i, "").trim();
  if (stripped !== songTitle && catalog[stripped]) return catalog[stripped];
  return null;
}

/**
 * Official music-video links for setlist songs (used when the setlist is complete).
 * @param {string} artistSlug
 * @param {string[]} setlist
 * @param {{ setlistComplete?: boolean }} [opts]
 * @returns {Record<string, string>}
 */
export function buildConcertVideos(artistSlug, setlist, opts = {}) {
  const { setlistComplete = true } = opts;
  if (!setlistComplete || !setlist.length) return {};
  /** @type {Record<string, string>} */
  const out = {};
  for (const song of setlist) {
    const url = getOfficialVideo(artistSlug, song);
    if (url) out[song] = url;
  }
  return out;
}
