const USER_AGENT = "LiveKonzertCompanion/1.0 (private project; asset fetch)";

export async function fetchItunesArtwork(query) {
  const url = `https://itunes.apple.com/search?term=${encodeURIComponent(query)}&entity=album&limit=1&country=de`;
  const res = await fetch(url, { headers: { "User-Agent": USER_AGENT } });
  if (!res.ok) return null;
  const data = await res.json();
  const art = data.results?.[0]?.artworkUrl100;
  if (!art) return null;
  return art.replace("100x100bb", "600x600bb");
}

export async function fetchCoverArtRelease(releaseId) {
  return `https://coverartarchive.org/release/${releaseId}/front-500`;
}

export async function fetchDeezerArtistPicture(query) {
  const url = `https://api.deezer.com/search/artist?q=${encodeURIComponent(query)}`;
  const res = await fetch(url, { headers: { "User-Agent": USER_AGENT } });
  if (!res.ok) return null;
  const data = await res.json();
  const pic = data.data?.[0]?.picture_xl;
  if (!pic || pic.includes("d41d8cd98f00b204e9800998ecf8427e")) return null;
  return pic;
}
