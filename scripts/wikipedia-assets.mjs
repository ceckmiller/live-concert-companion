const USER_AGENT = "LiveKonzertCompanion/1.0 (private project; asset fetch)";

export async function fetchWikiThumb(lang, title, size = 500) {
  const api = `https://${lang}.wikipedia.org/w/api.php?action=query&titles=${encodeURIComponent(title)}&prop=pageimages&format=json&pithumbsize=${size}&origin=*`;
  const res = await fetch(api, { headers: { "User-Agent": USER_AGENT } });
  if (!res.ok) return null;
  const data = await res.json();
  const page = Object.values(data.query?.pages ?? {})[0];
  if (!page || page.missing !== undefined) return null;
  return page.thumbnail?.source ?? null;
}

export async function fetchWikiThumbAny(entries, size = 500) {
  const list = Array.isArray(entries) ? entries : [entries];
  for (const entry of list) {
    const thumb = await fetchWikiThumb(entry.lang, entry.title, size);
    if (thumb) return thumb;
  }
  return null;
}

export async function downloadUrl(url, destPath) {
  const res = await fetch(url, { headers: { "User-Agent": USER_AGENT } });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  const buf = Buffer.from(await res.arrayBuffer());
  const { writeFileSync, mkdirSync } = await import("fs");
  const { dirname } = await import("path");
  mkdirSync(dirname(destPath), { recursive: true });
  writeFileSync(destPath, buf);
}

export function slugifyFile(text) {
  return String(text)
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 60);
}
